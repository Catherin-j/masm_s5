.MODEL SMALL
.STACK 100H
.DATA
 MSG1 DB 'ENTER THE STRING: $'
 MSG2 DB 0DH,0AH,'STRLEN: $'
 BUF DB 20
     DB 0
     DB 20('$')
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX

  ; PRINT MSG1
LEA DX,MSG1
MOV AH,09h
INT 21h

  ;TAKE I/P
MOV DX, offset BUF
MOV AH,0AH
INT 21H

  ;BUF+1 WILL HAVE COUNT
MOV CL,BUF+1

  ;PRINT MSG2
LEA DX,MSG2
MOV AH,09h
INT 21H

; THE ANS IS IN CL
MOV DL,CL
ADD DL,'0'
MOV AH,02h
INT 21H

MOV AH,4CH
INT 21h
MAIN ENDP
END MAIN


