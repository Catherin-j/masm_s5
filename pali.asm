.MODEL SMALL
.STACK 100h
.DATA
STR  DB 'AMMA$',0 ; string
MSG2 DB 'PALINDROME$',0
MSG3 DB 'NOT PALINDROME$',0

.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX

;ASSIGNN POINTERS 
LEA SI,STR
MOV DI,SI

;ASSING DI TO END OF STR

 FIND_END:
 MOV AL,[DI]
 CMP AL,'$'
 JE FOUND
 INC DI
 JMP FIND_END

FOUND:
 DEC DI

; COMPARE

 COMPARE_LOOP:
 CMP SI,DI 
 JGE TRUE_COND

 MOV AL,[SI]
 MOV BL,[DI]
 CMP AL,BL
 JE EQUAL
 JNE NOT_EQUAL

 EQUAL:
 INC SI
 DEC DI
 JMP COMPARE_LOOP

 NOT_EQUAL:
 LEA DX,MSG3
 MOV AH,09h
 INT 21H
 JMP EXIT_PG

 TRUE_COND:
 LEA DX,MSG2
 MOV AH,09h
 INT 21H

  EXIT_PG:
  MOV AH,4Ch
  INT 21H
MAIN ENDP
END MAIN
