.MODEL SMALL
.STACK 100h
.DATA
MSG1 DB 'ENTER A NO : $'
MSG2 DB 'EVEN NO :$'
MSG3 DB 'ODD NO : $'
MSG4 DB ' NO IS ZERO $'
NEWLINE DB 0DH,0AH,'$'
.CODE
MAIN PROC 
MOV AX,@DATA
MOV DS,AX


LEA DX,MSG1
MOV AH,09h
INT 21H
 ;TAKE INPUT
MOV AH,01H
INT 21H 
SUB AL,'0'
MOV BL,AL

CMP BL,0
JE DISPLAY_ZERO 

NO_CHECK:
MOV AL,BL
XOR AH,AH
MOV CL,2
DIV CL ; AL=Q,AH=R=MOD

CMP AH,0
JE DISPLAY_EVEN
JNE DISPLAY_ODD
JMP END_SWITCH

DISPLAY_EVEN:
LEA DX,NEWLINE
MOV AH,09h
INT 21H

LEA DX,MSG2
MOV AH,09h
INT 21H
JMP END_SWITCH

DISPLAY_ODD:
LEA DX,NEWLINE
MOV AH,09h
INT 21H

LEA DX,MSG3
MOV AH,09h
INT 21H
JMP END_SWITCH

DISPLAY_ZERO:
LEA DX,MSG4
MOV AH,09h
INT 21H
JMP END_SWITCH

 END_SWITCH:
 MOV AH,4Ch
 INT 21h

MAIN ENDP
END MAIN