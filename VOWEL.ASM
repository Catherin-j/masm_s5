.MODEL SMALL
.STACK 100h
.DATA
MSG1 DB 'ENTER A CHAR: $'
MSG2 DB 'VOWEL!!$'
MSG3 DB 'NOT VOWEL!!$'
BUF DB 21
    DB ?
    DB 21 DUP('$')
NEWLINE DB 0DH,0AH,'$'
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX

LEA DX,MSG1
MOV AH ,09h
INT 21H
 ;TAKE INPUT
 LEA DX,BUF
 MOV AH,0AH
 INT 21H


MOV AL,[BUF+2];ACTUAL I/P
 CMP AL,'A'
 JE VOWEL
  CMP AL,'E'
 JE VOWEL
  CMP AL,'I'
 JE VOWEL
  CMP AL,'O'
 JE VOWEL
  CMP AL,'U'
 JE VOWEL
JMP CONSONENT

VOWEL:
LEA DX,MSG2
MOV AH ,09h
INT 21H
JMP END_SWITCH

CONSONENT:
LEA DX,MSG3
MOV AH ,09h
INT 21H
JMP END_SWITCH

 END_SWITCH:
 MOV AH,4Ch
 INT 21H
MAIN ENDP
END MAIN