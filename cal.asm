.MODEL SMALL
.STACK 100h
.DATA
MSG1 DB 'ENTER DIGIT1: $'
MSG2 DB 'ENTER DIGIT2: $'
MSG3 DB 'RESULT: $'
MSG4 DB 'SELECT OPERATION: $'
MSG5 DB '1.ADD 2.SUB 3.MUL 4.DIV $'
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX

 ; PRINT MSG1
LEA DX,MSG1
MOV AH,09h
INT 21H
 ;TAKE I/P
MOV AH,01H
INT 21H
SUB AL,'0'
MOV BL,AL ; I/P1 =BL

 ;PRINT MSG2
LEA DX,MSG2
MOV AH,09h
INT 21H
 ; TAKE I/P
MOV AH,01H
INT 21H
SUB AL,'0'
MOV CL,AL ; I/P2=CL

;PRINT MSG4,5
LEA DX,MSG4
MOV AH,09h
INT 21H
LEA DX,MSG5
MOV AH,09h
INT 21H
 ;TAKE I/P OPERATION
 MOV AH,01H
 INT 21H 
 SUB AL,'0'

CMP AL,1
JE CASE1
CMP AL,2
JE CASE2
CMP AL,3
JE CASE3
CMP AL,4
JE CASE4
JMP END_SWITCH

CASE1:
MOV AL,BL
ADD AL,CL
JMP DISPLAY

CASE2:
MOV AL,BL
SUB AL,CL
JMP DISPLAY

CASE3:
MOV AL,BL
MUL CL

JMP DISPLAY

CASE4:
MOV AL,BL
XOR AH,AH ; CLEAR AH SO AH=AL
DIV CL  ; O/P IS IN AX

JMP DISPLAY

DISPLAY:
;ALL ANS ARE IN 'AL'

LEA DX,MSG3
MOV AH,09h
INT 21H

ADD AL,'0'
MOV DL,AL
MOV AH,02H ; MOV O/P TO DL
INT 21H
JMP END_SWITCH

 END_SWITCH:
 MOV AH,4Ch
 INT 21h

MAIN ENDP 
END MAIN