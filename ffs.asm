.MODEL SMALL
.STACK 100H 
.DATA
MSG1 DB 'FIB SERIES: $'
COUNT DW 5
TEMP DW 0
FIB1 DW 0
FIB2 DW 1
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX

MOV CX, COUNT
MOV AX,FIB1
CALL PRINT_NO
MOV AX,FIB2
CALL PRINT_NO
SUB CX,2

FIB_LOOP:
MOV AX,FIB1
ADD AX,FIB2
MOV TEMP,AX

CALL PRINT_NO

MOV AX,TEMP

MOV AX,FIB2
MOV FIB1,AX ;FIB1=FIB2
MOV AX,TEMP
MOV FIB2,AX ;FIB2=TEMP

 LOOP FIB_LOOP

    MOV AH, 4Ch         ; Exit to DOS
    INT 21h
MAIN ENDP

PRINT_NO PROC
 PUSH AX
 PUSH BX
 PUSH CX
 PUSH DX
 XOR CX,CX
 MOV BX,10

 CONVERT_LOOP:
 XOR DX,DX
 DIV BX
 PUSH DX
 INC CX
 CMP AX,0
 JNE CONVERT_LOOP

 PRINT:
 POP DX
 ADD DL,'0'
 MOV AH,02h
 INT 21H
 LOOP PRINT
 POP DX
 POP CX
 POP BX
 POP AX
 RET
 PRINT_NO ENDP
 END