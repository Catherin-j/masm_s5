.MODEL SMALL
.STACK 100h
.DATA
STR  DB 'AMMA$',0 ; string
MSG2 DB 0DH,0AH ,'STR_REV$',0
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX

LEA SI,STR
MOV DI,SI

FIND_END:
MOV AL,[DI]
CMP AL,'$'
JE FOUND
INC DI
JMP FIND_END

FOUND:
DEC DI
LEA DX,MSG2 
MOV AH,09H 
INT 21H

PRINT_REV:
MOV AL,[DI]
MOV DL,AL  ;PRINT
MOV AH,09h
INT 21H
DEC DI
CMP DI,SI
JE EXIT
JMP PRINT_REV

EXIT: 
MOV AH,4CH 
INT 21H 
MAIN ENDP 
END MAIN
