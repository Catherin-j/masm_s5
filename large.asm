.MODEL SMALL
.STACK 100h
.DATA
A DB 4
B DB 7
C DB 2
MSG DB 'Largest = $'
.CODE
MAIN PROC

MOV AX,@DATA
MOV DS,AX

MOV AL,A 
CMP AL,B 
JGE SKIP1
MOV AL,B
SKIP1:
CMP AL,C 
JGE DONE
MOV AL,C


DONE:
LEA DX,MSG
MOV AH,09H
INT 21H
ADD AL,'0'
MOV DL,AL
MOV AH,02H
INT 21H

MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN
