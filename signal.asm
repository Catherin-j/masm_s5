.MODEL SMALL
.STACK 100h
.DATA
MSG0 DB 'ENTER A NO: $'
MSG1 DB 'RED:STOP!$'
MSG2 DB 'YELLOW:WAIT!$'
MSG3 DB 'GREEN:GO!$'

.CODE
MAIN PROC 
MOV AX,@DATA
MOV DS,AX

LEA DX,MSG0
MOV AH,09h
INT 21h

MOV AH,01H
INT 21h
MOV BL,AL

CMP BL,'1'
JE RED
CMP BL,'2'
JE YELLOW
CMP BL,'3'
JE GREEN

JMP END_SWITCH

RED:
LEA DX,MSG1
MOV AH,09h
INT 21h
JMP END_SWITCH
YELLOW:
LEA DX,MSG2
MOV AH,09h
INT 21h
JMP END_SWITCH
GREEN:
LEA DX,MSG3
MOV AH,09h
INT 21h
JMP END_SWITCH
END_SWITCH:
MOV AH,4Ch
INT 21h

MAIN ENDP
END MAIN
