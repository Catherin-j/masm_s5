.MODEL SMALL
.STACK 100h
.DATA
MSG DB 'Fibonacci Series: $'
F1 DB 0
F2 DB 1
COUNT DB 6
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX

LEA DX,MSG
MOV AH,09H
INT 21H

MOV AL,F1
MOV DL,AL
ADD DL,'0'
MOV AH,02H
INT 21H

MOV AL,F2
MOV DL,AL
ADD DL,'0'
MOV AH,02H
INT 21H

MOV CL,COUNT
SUB CL,2

NEXT:
MOV AL,F1
ADD AL,F2
MOV BL,F2
MOV F1,BL
MOV F2,AL
ADD AL,'0'
MOV DL,AL
MOV AH,02H
INT 21H
DEC CL
JNZ NEXT

MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN
