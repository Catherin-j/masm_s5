.MODEL SMALL
.STACK 100h
.DATA
MSG0 DB 'ENER THE DAY_NO: $'
MSG1 DB 'SUNDAY$'
MSG2 DB 'MONDAY$'
MSG3 DB 'TUEDAY$'
MSG4 DB 'WEDNESDAY$'
MSG5 DB 'THRUSDAY$'
MSG6 DB 'FRIDAY$'
MSG7 DB 'SATURDAY$'
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX

LEA DX,MSG0
MOV AH,09h
INT 21H

;TAKE Input
MOV AH,01H
INT 21h
MOV BL,AL
SUB BL,'0';ASCII TO DIGIT

CMP BL,1
JE CASE1

CMP BL,2
JE CASE2

CMP BL,3
JE CASE3

CMP BL,4
JE CASE4

CMP BL,5
JE CASE5

CMP BL,6
JE CASE6

CMP BL,7
JE CASE7

JNE EXIT_SWITCH

CASE1:
LEA DX,MSG1
MOV AH,09h
INT 21h
JMP EXIT_SWITCH
CASE2:
LEA DX,MSG2
MOV AH,09h
INT 21h
JMP EXIT_SWITCH
CASE3:
LEA DX,MSG3
MOV AH,09h
INT 21H
JMP EXIT_SWITCH
CASE4:
LEA DX,MSG4
MOV AH,09h
INT 21h
JMP EXIT_SWITCH
CASE5:
LEA DX,MSG5
MOV AH,09h
INT 21h
JMP EXIT_SWITCH
CASE6:
LEA DX,MSG6
MOV AH,09h
INT 21h
JMP EXIT_SWITCH
CASE7:
LEA DX,MSG7
MOV AH,09h
INT 21h
JMP EXIT_SWITCH

 EXIT_SWITCH:
 MOV AH,4Ch
 INT 21H
MAIN ENDP
END MAIN