.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB 'ENTER THE INPUT: $'
MSG2 DB 'Excellent$'
MSG3 DB 'GOOD$'
MSG4 DB 'AVG$'
BUF DB 21          ;MAX_LENGTH
    DB ?           ;LENGTH
    DB 21 DUP('$') ;1ST CHAR TYPED
NEWLINE DB 0DH,0AH,'$'
.CODE
MAIN PROC 
MOV AX,@DATA
MOV DS,AX

LEA DX,MSG1
MOV AH,09H
INT 21h
 ;TAKE INPUT
LEA DX,BUF
MOV AH,0AH
INT 21H

LEA DX,NEWLINE
MOV AH,09h
INT 21H
MOV AL,BUF+2;1ST CHAR TYPED WILL BE AT BUF+2

CMP AL,'A'
JE Excellent
CMP AL,'B'
JE GOOD
CMP AL,'C'
JE AVG
JMP END_SWITCH

Excellent:
LEA DX,MSG2
MOV AH,09H
INT 21h
JMP END_SWITCH

GOOD:
LEA DX,MSG3
MOV AH,09H
INT 21h
JMP END_SWITCH

AVG:
LEA DX,MSG3
MOV AH,09H
INT 21h
JMP END_SWITCH

 END_SWITCH:
 MOV AH,4Ch
 INT 21H
MAIN ENDP
END MAIN